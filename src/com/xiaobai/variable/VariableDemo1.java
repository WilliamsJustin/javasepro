package com.xiaobai.variable;

public class VariableDemo1 {
    public static void main(String[] args) {
//        目标：学会使用变量
//        数据类型 变量名称 = 初始值
        double money =6.0;
        System.out.println(money);

//    收红包(从=右往左看)
        money = money + 4.0;
        System.out.println(money);
    }
}
